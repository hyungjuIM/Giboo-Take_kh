
/* 등급별 혜택 클릭시 혜택 상세내용 slideToggle */


/*혜택1*/
$(document).ready(function(){

    $("#icon_down_1").click(function(){
        $("#benefit_down_1").slideDown('fast');
        $('#icon_down_1').hide();
        $('#icon_up_1').show();
    })

    $("#icon_up_1").click(function(){
        $("#benefit_down_1").slideUp('fast');
        $('#icon_up_1').hide();
        $('#icon_down_1').show();
    })
})


